package model;

public class WaterUsageByAggregation
{
	private String name;
	private double usage; // in litres
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public double getUsage()
	{
		return usage;
	}
	public void setUsage(double usage)
	{
		this.usage = usage;
	}
}
